18/10/2009<br>
</span><div style="margin-left: 40px;"><span style="font-weight: bold;"></span>Those
who've
been reading the forum and checking the Latest Build page will already
know this, but GCBASIC now has almost full support for AVR
microcontrollers. Don't count on it for anything important just yet,
but if you'd like to try programming an AVR with GCBASIC, please give
it a go. If you find a bug, please let us know on the forum. You can
obtain the latest build from the Download page, or from the Latest
Build page.<br><br>
The other news is that a completely new program has been released.
Great Cow Graphical BASIC is an open source icon based editor for
GCBASIC programs. Drag and drop the icons to make up a GCBASIC program,
then send it to your PIC or AVR. Find out more about it from the About
GCGB page, or download it from the Download page. It's still under
development and is at the alpha stage, any bug reports or suggestions
are most welcome at the forum.<br><br>
